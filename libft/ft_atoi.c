#include "libft.h"

int	ft_atoi(const char *str)
{
	int	numbers;
	int	i;
	int	sign;

	i = 0;
	numbers = 0;
	sign = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		numbers = (numbers * 10) + (str[i] - '0');
		i++;
	}
	return (sign * numbers);
}
